package Class2;

public class E1LoopDefine {

    public static void main(String[] args) {


        // There three different types of loops
        //1. for loop =
        //2. while loop =
        //3. do while loop =

        // A. for loop: I want to print Hello 4 times:

        for(int i=0; i<4; i++){
            System.out.println("Hello");

        }
        //B. while loop: I want to print how are you 4 times.

        int x=1;
        while(x<=4){
            System.out.println("How are you");
            x++;
        }

        //C. do while:
        int a=0;
        do {
            System.out.println("I am doing great");
            a ++;
        }while(a<=4);
            System.out.println(a);
        }
    }

