package Class2;

public class E5Loops {
    public static void main(String[] args) {


        int x = 1;
        while (x <= 99) {
            System.out.println(x+" ");
            x=x+2;
        }
        int y=100;
        while(y>=1){
            System.out.println(y+" ");
            y=y-2;
        }
    }
}